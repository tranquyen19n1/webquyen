                        <h2>Cơ sở vật chất</h2>

                        <p>Trường hiện nay có 2 cơ sở, với cơ sở vật chất và hạ tầng kỹ thuật đáp ứng được điều kiện phục vụ đào tạo tương ứng với qui mô phát triển của Nhà truờng.</p>

