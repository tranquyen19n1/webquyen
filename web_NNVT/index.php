                        <h2>Trung tâm Thông tin – Thư viện</h2>

                        <p>Với diện tích 2.623 m2 thư viện nhà trường bao gồm thư viện đọc và thư viện điện tử đã được số hóa, kết nối trực trực tuyến với 26 đơn vị thành viên trong cả nước, thư viện được trang bị đầy đủ giáo trình, tài liệu tham khảo phục vụ giảng dạy, học tập và nghiên cứu khoa học.</p>

                        <h2>Thư viện điện tử</h2>

                        <p>Thư viện điện tử : TVĐT với 200 máy tính được trang bị hiện đại, cấu hình mạnh, kết nối mạng Internet không dây, có nguồn tài nguyên được số hóa dưới nhiều hình thức gồm có 226.560 tài liệu được số hóa dưới dạng PDF. Số tài liệu kể trên bao gồm nhiều lĩnh vực như: khoa học tự nhiên, khoa học kỹ thuật và công nghệ, nghiên cứu sáng chế, khoa học xã hội và nhân văn, kinh tế v.v.</p>

