                        <p>Thư viện danh mục các kết quả nghiên cứu: Gồm các báo cáo khoa học dưới dạng rút gọn và toàn văn với tổng số 9.574 tài liệu, trong đó có 6.155 tài liệu toàn văn. Các tài liệu này được lưu trữ trên máy chủ, tổ chức theo công nghệ Internet cục bộ. Giao diện khai thác được xây dựng dưới dạng Web.</p>

                        <p>Cơ sở dữ liệu ProQuest: Là dạng cơ sở dữ liệu trực tuyến. Đây là cơ sở dữ liệu toàn văn tổng hợp lớn nhất hiện nay với 26 đơn vị thành viên như: Cục Thông tin Khoa học và Công nghệ quốc gia, trường đại học quốc gia TP HCM, trường đại học quốc gia Hà Nội, đại học Đà Nẵng, đại học Bách khoa Hà Nội, trường ĐHCN Việt Trì.</p>

