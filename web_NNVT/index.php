                            <li>Cơ sở Việt Trì: Số 9, đường Tiên Sơn, phường Tiên Cát, thành phố Việt Trì, tỉnh Phú Thọ.</li>
                            <li>Cơ sở Lâm Thao: xã Tiên Kiên, huyện Lâm Thao, tỉnh Phú Thọ</li>
